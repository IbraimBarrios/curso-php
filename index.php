<?php 
//Operadores incremento y decremento

// Operador pre-incremento, aumenta 1 mas
$numero = 10;

$numero = $numero + 1;
$numero += 1;

echo ++$numero;

// Operador pos-incremento
$numero2 = 20;
$numero2++;
echo $numero2;

// Operador pre-decremento, decrementa uno menos
$numero3 = 20;
echo --$numero3;

// Operador pos-decremento,
$numero4 = 20;
$numero4--;
echo $numero4;
?>