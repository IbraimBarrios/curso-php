<?php 
// Switch

$camisa = "Verde";

switch ($camisa) {
  case "Gris":
    echo "Encontre Camisa gris";
    break;
  case "Amarillo":
    echo "Encontre Camisa Amarilla";
    break;
  case "Rojo":
    echo "Encontre Camisa Roja";
    break;
  case "Verde":
    echo "Encontre Camisa Verde";
    break;
  default:
  echo "No se encontro un color de camisa";
}

?>